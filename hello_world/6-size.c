#include <stdio.h>

/**
 * main - Entry point
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
  printf("Size of char data type: %u byte(s)\n", sizeof(char));
  printf("Size of int data type: %u byte(s)\n", sizeof(int));
  printf("Size of float data type: %u byte(s)\n", sizeof(float));
  printf("Size of double data type: %u byte(s)\n", sizeof(double));

  return (0);
}
